package br.ufg.inf.mestrado.hermesbase.model;

public class TriplaRDF {

	private String instancia_sujeito;
	private String instancia_predicado;
	private String instancia_objeto;
	private String tipo_sujeito;
	private String tipo_predicado;
	private String tipo_objeto;
	
	public String getInstancia_sujeito() {
		return instancia_sujeito;
	}
	public void setInstancia_sujeito(String instancia_sujeito) {
		this.instancia_sujeito = instancia_sujeito;
	}
	public String getInstancia_predicado() {
		return instancia_predicado;
	}
	public void setInstancia_predicado(String instancia_predicado) {
		this.instancia_predicado = instancia_predicado;
	}
	public String getInstancia_objeto() {
		return instancia_objeto;
	}
	public void setInstancia_objeto(String instancia_objeto) {
		this.instancia_objeto = instancia_objeto;
	}
	public String getTipo_sujeito() {
		return tipo_sujeito;
	}
	public void setTipo_sujeito(String tipo_sujeito) {
		this.tipo_sujeito = tipo_sujeito;
	}
	public String getTipo_predicado() {
		return tipo_predicado;
	}
	public void setTipo_predicado(String tipo_predicado) {
		this.tipo_predicado = tipo_predicado;
	}
	public String getTipo_objeto() {
		return tipo_objeto;
	}
	public void setTipo_objeto(String tipo_objeto) {
		this.tipo_objeto = tipo_objeto;
	}
	
	
}
